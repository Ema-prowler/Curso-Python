nombre = "ema"

print(len(nombre)) # tama√±o de la cadena de caracteres
print(nombre.find("a")) # encontrar una letra
print(nombre.capitalize()) # capitalizar la primera letra
print(nombre.upper()) # poner todo el texto en mayusculas
print(nombre.lower()) # poner todo el texto en minuscula
print(nombre.isdigit()) # saber si esta compuesta de digitos
print(nombre.isalpha()) # saber si solo contiene caracteres alfabeticos
print(nombre.count("a")) #contar cuantas letras hay en la cadena (case sensitive)
print(nombre.replace("e","i")) # Reemplazar una letra de la cadena por otra letra
print(nombre*4) # imprimir la variable 4 veces
