import math

PI = 3.14
x = 2
y = 1
z = 3

print(round(PI)) # redondeo
print(math.ceil(PI)) # redondeo hacia arriba
print(math.floor(PI)) # redondeo hacia abajo
print(abs(PI)) # valor positivo
print(pow(PI,2)) # sacar potencia de un numero
print(math.sqrt(420)) # raiz cuadrada o otra raiz
print(max(x,y,z)) # el numero maximo o el mas grande
print(min(x,y,z)) # el numero minimo o el mas chico

